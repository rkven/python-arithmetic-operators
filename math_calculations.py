# Basic functionsfor math calculations


def addition(num1,num2):
    x= float(num1)
    y=float(num2)
    print (x + y)

def division(num1,num2):
    x= float(num1)
    y=float(num2)
    print(x/y)

def div_reminder(num1,num2):
    x= float(num1)
    y=float(num2)
    print(x%y)
    
def multiplication(num1,num2):
    x= float(num1)
    y=float(num2)
    print(x*y)

def power(num1, exponent):
    x=float(num1)
    print(x**exponent)

def floor_division(num1,num2):
    x= float(num1)
    y=float(num2)
    print(x//y)